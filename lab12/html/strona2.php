	<h2 style="margin-left: 10px; text-align: center;"> 2 MIEJSCE </h2>
	<img src="../img/miejsce2.jpg" ALIGN="left" style="margin: 10px; width: 300px;">
	
	Lake Pontchartrain, Stany Zjednoczone
</b>
<b>Długość:</b> 38,35 km <br>
Dwa równoległe mosty poprowadzono nad jeziorem Pontchartrain w południowej części stanu Luizjana.
Dłuższy ma 38,35 km długości. Południowy koniec znajduje się w Metairie w Luizjanie, na przedmieściach 
Nowego Orleanu. Północny w Mandeville w Luizjanie. Przeprawy są wsparte na 9500 betonowych palach. 
Oba mają przęsła zwodzone na potrzeby żeglugowej drogi wodnej.
Południową przeprawą budowano od 1955 r., a oddano do użytku 30 sierpnia 1956 r. Budowa północnej 
rozpoczęła się w 1967 r., a otwarto ją w 1969 r. Obie konstrukcje dzieliło 14 lat. Koszt mostów wyniósł 
łącznie 76 mln dol. z tamtych czasów. Pomost znajduje się na wysokości 4,7 m nad poziomem wody. Mosty mają 
dwie dwupasowe jezdnie, a ich szerokość wynosi 8,5 m.
Wykonawcą południowej nitki było przedsiębiorstwo Louisiana Bridge Company, a północnej Prestressed 
Concrete Products. Nowsza jest o 15 m dłuższa, a odstęp między mostami wynosi 25,6 m.
Inwestycja pozwoliła skrócić podróże z małych miejscowości North Shore do Nowego Orleanu o 50 min.

<b>
	<h2 style="margin-left: 10px; text-align: center;"> 3 MIEJSCE </h2>
	<img src="../img/miejsce3.jpg" ALIGN="left" style="margin: 10px; width: 300px;">
	Sheikh Jaber Al-Ahmad Al-Sabah, Kuwejt
</b>
<b>Długość:</b> 37 km <br>
Sheikh Jaber Al-Ahmad Al-Sabah znajduje się w Kuwejcie na Bliskim Wschodzie. 
Nazwa upamiętnia prezydenta Kuwejtu Emira, który był u władzy podczas wojny w Zatoce Perskiej. 
Most przekracza Zatokę Kuwejcką i łączy Kuwejt z Madinat al-Hareer (Jedwabne Miasto). 
Budowano go od listopada 2013 r. przez cztery lata, ale otwarto dopiero 1 maja 2019 r. 
Inwestycja oznaczała wydatek 3 mld dol. (całość licząca 48,5 km z drogami i wiaduktem na lądzie).
Przy długości mostu 37 km bezpośrednio nad wodą znajduje się część licząca 34,9 km. Pylon wznosi się 
na 151 m (jest inspirowany żaglami tradycyjnymi statków), najdłuższe przęsło ma 177 m, pomost znajduje 
się na wysokości 23 m nad poziomem morza. Na obu nitkach znajdują się trzypasowe jezdnie z dodatkowym 
pasem bezpieczeństwa. Podczas robót powstały dwie sztuczne wyspy o powierzchniach 300 tys. m2 i 600 tys. m2.
Most jest nadzorowany przez system SCADA, a ruch i sytuację na przeprawie śledzi 600 kamer.
Inne nazwy przeprawy to Kuwait Bay Bridge i Jaber link Doha Bridge. Właścicielem obiektu jest kuwejckie 
Ministerstwo Robót Publicznych, projekt opracowała spółka Systra, a wykonawcą była firma Hyundai Engineering 
& Construction. Żywotność mostu wynosi 100 lat.
