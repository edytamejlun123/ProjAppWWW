	<h2 style="margin-left: 10px; text-align: center;">1 MIEJSCE</h2></b>
	<img src="../img/miejsce1.jpg" ALIGN="left" style="margin: 10px; width: 300px">
	<b> Hong Kong-Zhuhai-Macau, Chiny
		</b><br>
	<b>Długość:</b> 41,6 km <br>

	Most z tunelem łączy Hongkong, Guangdong Zhuhai i Makau. Przeprawa biegnie od sztucznej wyspy w pobliżu lotniska
	Hong Kong International Airport na wschód, przekracza obszar Lingdingyang w delcie Rzeki Perłowej na wodach Morza 
	Południowochińskiego, komunikuje Zhuhai i sztuczną wyspę w Makau, i dociera do terminalu Zhuhai Hongwan.
	Budowę prowadzono w latach 2009–2017. Obiekt otwarto 18 października 2018 r.Długość od portu w Hong Kongu 
	do portu Zhuhai i portu w Makau wynosi około 42 km i obejmuje 12-kilometrową przeprawę Hong Kong Link Road i liczący 
	29,6 km most główny. Całkowita długość Hong Kong-Zhuhai-Macau (HZMB) wynosi 55 km, wliczając tunel Zhuhai Link Road i 
	drogę . Przez obiekt prowadzi droga z sześcioma pasami na dwóch jezdniach (po trzy). Dopuszczalna prędkość to 100 km/godz.
	Most jest przedzielony liczącym 5,7 km tunelem o szerokości 28,5 m i wysokości 5,1 m. Przeprawa skłąda się z 224 pomostów i 
	siedmiu pylonów. Jest szeroka na 33,1 m. Zaprojektowany okres użytkowania mostu wynosi 120 lat. Obiekt może wytrzymać 
	trzęsienie ziemi o sile 8, tajfun o sile 16, uderzenie o masie 300 tys. ton i 300-letnią powódź w ujściu rzeki Perłowej.
	Inwestycja pochłonęła niebotyczną sumę 18,8 mld dol.

